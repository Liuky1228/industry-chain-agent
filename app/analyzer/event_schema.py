"""产业链事件（脉冲）数据模型与抽取规范

参考 cwwindex.today 的 "72小时产业链脉冲" 设计理念：
- 事件为「已验证/已报道的事实事件证据流」，不做情感/利好利空判断
- 每条事件必须可回溯来源、标注证据状态、带去情绪化护栏
- 抓不到高信号事件时，显示诚实的空状态，绝不编造填充

本模块同时被后端抽取（extractor.extract_events）与前端展示（EventTimeline）复用，
是事件功能的「单一事实来源」。
"""

# ─────────────────────────────────────────────
#  受控词表（设计点⑤：固定标签替代自由文本事件类型，便于筛选与聚合）
# ─────────────────────────────────────────────
EVENT_TAGS = [
    "产能扩张",   # 新建 / 扩产 / 投产 / 产能落地
    "政策法规",   # 产业政策、规划、补贴、监管
    "并购重组",   # 收购、合并、分拆、战略投资
    "技术突破",   # 新品发布、技术节点、认证、量产良率
    "价格变动",   # ASP / 报价 / 合约价变动（仅事实，不含涨跌判断）
    "合规调查",   # 诉讼、调查、制裁、反倾销
    "合作生态",   # 战略合作、供货协议、生态绑定
]

# ─────────────────────────────────────────────
#  证据状态（与设计点①关系证据化共用同一套信任机制）
# ─────────────────────────────────────────────
EVIDENCE_STATUS = ["VERIFIED", "REPORTED", "INFERRED", "UNVERIFIED"]

EVIDENCE_STATUS_LABELS = {
    "VERIFIED": "已验证",    # 一手官方 / 监管披露
    "REPORTED": "已报道",    # 二手可信来源（如研报、权威媒体）
    "INFERRED": "推断",      # 由 LLM 基于文本推断，非原文明示
    "UNVERIFIED": "未核实",  # 来源不明或待核实
}

# 当前数据源为「研报」（二手），故默认证据状态统一为 REPORTED，
# 不应出现 VERIFIED（我们没有一手披露链接）。
DEFAULT_EVIDENCE_STATUS = "REPORTED"

# ─────────────────────────────────────────────
#  事件抽取政策（翻自 72H JSON 的 policy 块）
#  作为我们事件抽取的硬约束 + 前端免责声明来源
# ─────────────────────────────────────────────
EVENT_POLICY = {
    # 我们来源是研报（二手），无法强制一手来源
    "primarySourcesRequired": False,
    # 不收录未经证实的传闻
    "rumorsAllowed": False,
    # 不推断利好 / 利空或股价 / 指数影响
    "marketImpactInferred": False,
    # 无高信号事件时显空状态，不编造
    "emptyWindowBehavior": "PUBLISH_VERIFIED_EMPTY_STATE",
    "disclaimer": (
        "本板块仅列已验证/已报道的事实事件，不构成任何投资建议；"
        "AI 不判断利好或利空，事件影响以原始披露为准。"
    ),
}

# ─────────────────────────────────────────────
#  影响护栏模板（每条事件 impactZh 必须包含）
# ─────────────────────────────────────────────
IMPACT_GUARDRAIL = (
    "本事件仅为事实陈述，不等同于订单金额、价格涨幅、官方指数或股价变动依据；"
    "已报道不代表确定结论，具体以原始披露为准。"
)

# 默认事件窗口天数（由 main.py 按爬取 date_range_days 覆盖）
DEFAULT_EVENT_WINDOW_DAYS = 90
